import Foundation

enum HTTPMethod {
    case get, delete, options, post, put, patch
    
    var description: String {
        switch self {
        case .get:
            return "GET"
        case .post:
            return "POST"
        case .put:
            return "PUT"
        case .patch:
            return "PATCH"
        case .delete:
            return "DELETE"
        case .options:
            return "OPTIONS"
        }
    }
}
